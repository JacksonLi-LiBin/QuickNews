package com.lb.quicknews.bean;

public class PictureDetailModle extends BaseModle {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8099343684254746551L;
	private String title;
	private String pic;
	private String alt;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getPic() {
		return pic;
	}

	public void setPic(String pic) {
		this.pic = pic;
	}

	public String getAlt() {
		return alt;
	}

	public void setAlt(String alt) {
		this.alt = alt;
	}

}
