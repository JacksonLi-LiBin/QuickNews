package com.lb.quicknews.bean;

public class PictureModle extends BaseModle {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6363635586630986990L;
	private String id;
	private String title;
	private String pic;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getPic() {
		return pic;
	}

	public void setPic(String pic) {
		this.pic = pic;
	}

}
